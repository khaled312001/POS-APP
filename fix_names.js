async function fixNames() {
  console.log('Fixing corrupted characters in customer names...\n');
  let total = 0;

  for (const [wrong, correct] of corrections) {
    if (!wrong.includes(R)) continue;
    try {
      const r = await pool.query(
        `UPDATE customers SET name = replace(name, $1, $2) WHERE name LIKE '%' || chr(65533) || '%'`,
        [wrong, correct]
      );
      if (r.rowCount > 0) {
        console.log(`  [${r.rowCount}] "${wrong.replace(/\uFFFD/g, '?')}" → "${correct}"`);
        total += r.rowCount;
      }
    } catch (e) {
      console.error(`Error: ${e.message}`);
    }
  }

  const rem = await pool.query(
    `SELECT COUNT(*) as cnt FROM customers WHERE name LIKE '%' || chr(65533) || '%'`
  );
  console.log(`\nDone! Total updates: ${total}`);
  console.log(`Remaining names with corrupted chars: ${rem.rows[0].cnt}`);

  if (Number(rem.rows[0].cnt) > 0) {
    const samples = await pool.query(
      `SELECT id, name FROM customers WHERE name LIKE '%' || chr(65533) || '%' LIMIT 30`
    );
    console.log('\nRemaining samples:');
    samples.rows.forEach(r => console.log(`  ID ${r.id}: ${JSON.stringify(r.name)}`));
  }

  await pool.end();
}

fixNames().catch(e => { console.error(e.message); pool.end(); });
